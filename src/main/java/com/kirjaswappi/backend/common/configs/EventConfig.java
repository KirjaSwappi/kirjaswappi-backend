package com.kirjaswappi.backend.common.configs;

import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.core.task.TaskExecutor;
import org.springframework.scheduling.annotation.EnableAsync;
import org.springframework.scheduling.concurrent.ThreadPoolTaskExecutor;

/**
 * Configuration for async event processing. Enables asynchronous event handling
 * to prevent blocking main business logic.
 */
@Configuration
@EnableAsync
public class EventConfig {

  /**
   * Task executor for processing inbox update events asynchronously. Configured
   * with appropriate thread pool settings for real-time notifications.
   */
  @Bean(name = "inboxEventExecutor")
  public TaskExecutor inboxEventExecutor() {
    ThreadPoolTaskExecutor executor = new ThreadPoolTaskExecutor();
    executor.setCorePoolSize(2);
    executor.setMaxPoolSize(5);
    executor.setQueueCapacity(100);
    executor.setThreadNamePrefix("inbox-event-");
    executor.setWaitForTasksToCompleteOnShutdown(true);
    executor.setAwaitTerminationSeconds(30);
    executor.initialize();
    return executor;
  }
}
